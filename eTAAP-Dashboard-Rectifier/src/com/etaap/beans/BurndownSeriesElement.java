package com.etaap.beans;

public class BurndownSeriesElement {
	private int y;
	private long x;
	private String keys;
	private String addedRemoved;
	
	public int getY() {
		return y;
	}
	public void setY(int y) {
		this.y = y;
	}
	public long getX() {
		return x;
	}
	public void setX(long x) {
		this.x = x;
	}
	public String getKeys() {
		return keys;
	}
	public void setKeys(String keys) {
		this.keys = keys;
	}
	public String getAddedRemoved() {
		return addedRemoved;
	}
	public void setAddedRemoved(String addedRemoved) {
		this.addedRemoved = addedRemoved;
	}
}
