package com.etaap.beans;

public class Status {
	String name;
	int y;
	String color;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getCount() {
		return y;
	}
	public void setCount(int count) {
		this.y = count;
	}
	public String getColor() {
		return color;
	}
	public void setColor(String color) {
		this.color = color;
	}
	
	
	
}
